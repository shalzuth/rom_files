local getBitResult = function ( ... )
	local res = 0
	local args = {...}
	local arg
	for i=1,#args do
		arg = args[i]
		if(arg and arg >0 and arg<=64) then
			res = res + 2^(arg-1)
		end
	end
	return res
end

